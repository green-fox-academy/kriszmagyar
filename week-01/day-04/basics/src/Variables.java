public class Variables {
    public static void main(String[] args) {

        // Favorite number
        int favoriteNumber = 23;
        System.out.println("\"My favorite number is: " + favoriteNumber + "\"");

        // Swap
        int a = 123;
        int b = 526;
        int tmp;

        tmp = a;
        a = b;
        b = tmp;

        System.out.println("a: " + a);
        System.out.println("b: " + b);

    }
}
