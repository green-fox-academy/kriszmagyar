public class Loops {
    public static void main(String[] args) {

        // I wont cheat on exams
        for (int i = 0; i < 100; i++) {
            System.out.println("I won't cheat on exam!");
        }

        // Print even
        for (int i = 2; i < 500; i += 2) {
            System.out.println(i);
        }

        // Multiplication table
        int num = 15;
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " * " + num + " = " + (i * num));
        }

    }
}
